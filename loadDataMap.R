CB.lat <- vector(mode="numeric",length=36+9)
CB.lng <- vector(mode="numeric",length=36+9)
for (i in 1:9) {
  lwr <- 5*(i-1)+1; upr <- lwr+4
  CB.lat[lwr:(upr-1)] <- blk.map.dat$Latitude[(4*(i-1)+1):(4*i)]
